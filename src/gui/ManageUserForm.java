    private void loadUser() {

    DefaultTableModel model =
    (DefaultTableModel) tblUser.getModel();

    model.setRowCount(0);

    try {

        ResultSet rs =
        UserManagerDB.getAllUsers();

        while(rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("id_user"),
                rs.getString("email"),
                rs.getString("username"),
                rs.getString("role"),
                rs.getInt("green_points")
            });

        }

    } catch(SQLException e) {
    }
}
    
    

