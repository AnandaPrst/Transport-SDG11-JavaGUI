    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
     int row = tblUser.getSelectedRow();

    if(row == -1){
        JOptionPane.showMessageDialog(
        this,
        "Pilih user terlebih dahulu");
        return;
    }

    int idUser =
    Integer.parseInt(
    tblUser.getValueAt(row,0).toString());

    if(UserManagerDB.deleteUser(idUser)){

        JOptionPane.showMessageDialog(
        this,
        "User berhasil dihapus");

        loadUser();
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnTambahUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahUserActionPerformed
    String email =
    JOptionPane.showInputDialog(
    "Masukkan Email");

    String username =
    JOptionPane.showInputDialog(
    "Masukkan Username");

    String password =
    JOptionPane.showInputDialog(
    "Masukkan Password");

    if(UserManagerDB.registerPenumpang(
            email,
            username,
            password)) {

        JOptionPane.showMessageDialog(
        this,
        "User berhasil ditambahkan");

        loadUser();

    } else {

        JOptionPane.showMessageDialog(
        this,
        "User gagal ditambahkan");
    }
    }//GEN-LAST:event_btnTambahUserActionPerformed

    private void btnEditUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditUserActionPerformed

    int row = tblUser.getSelectedRow();

    if(row == -1){

        JOptionPane.showMessageDialog(
            this,
            "Pilih user terlebih dahulu");

        return;
    }

    int idUser = Integer.parseInt(
        tblUser.getValueAt(row, 0).toString());

    String emailLama =
        tblUser.getValueAt(row, 1).toString();

    String usernameLama =
        tblUser.getValueAt(row, 2).toString();

    String emailBaru =
        JOptionPane.showInputDialog(
            this,
            "Edit Email",
            emailLama);

    String usernameBaru =
        JOptionPane.showInputDialog(
            this,
            "Edit Username",
            usernameLama);

    if(emailBaru == null ||
       usernameBaru == null){
        return;
    }

    if(UserManagerDB.updateUser(
            idUser,
            emailBaru,
            usernameBaru)) {

        JOptionPane.showMessageDialog(
            this,
            "User berhasil diupdate");

        loadUser();

    } else {

        JOptionPane.showMessageDialog(
            this,
            "User gagal diupdate");
    }
    }//GEN-LAST:event_btnEditUserActionPerformed

